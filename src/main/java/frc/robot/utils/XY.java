package frc.robot.utils;

/**
 * 2D data type with x and y components.
 */
public interface XY {
    /**
     * The x component of a 2D data type.
     * 
     * @return The x component
     */
    public double getX();

    /**
     * The y component of a 2D data type.
     * 
     * @return The y component
     */
    public double getY();
}
